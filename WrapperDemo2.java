public class WrapperDemo2{
    public static void main(String [] args){
        Integer obj_i=5;
        Float obj_f= 10.22f;
        Character obj_c='A';
        System.out.println(obj_i.intValue());
        System.out.println(obj_f.floatValue());
        System.out.println(obj_c.charValue());
    }
}